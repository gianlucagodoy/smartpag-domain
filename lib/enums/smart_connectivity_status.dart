enum SmartConnectivityStatus {
  /// Bluetooth: Dispositivo conectado via bluetooth
  bluetooth,

  /// WiFi: Dispositivo conectado via Wi-Fi
  wifi,

  /// Ethernet: Dispositivo conectado à rede de ethernet
  ethernet,

  /// Mobile: Dispositivo conectado à rede do celular
  mobile,

  /// None: Dispositivo não conectado a nenhuma rede
  none,
}
